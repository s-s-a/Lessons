# –°—á–∏—Ç—ã–≤–∞–µ–º –æ–¥–∏–Ω –∫–∞–¥—Ä —Å –∫–∞–º–µ—Ä—ã. 'ret' - –±—É–ª–µ–≤–æ –∑–Ω–∞—á–µ–Ω–∏–µ (True, –µ—Å–ª–∏ –∫–∞–¥—Ä —É—Å–ø–µ—à–Ω–æ —Å—á–∏—Ç–∞–Ω),

