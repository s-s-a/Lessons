if ret: # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª –ª–∏ —É—Å–ø–µ—à–Ω–æ —Å—á–∏—Ç–∞–Ω –∫–∞–¥—Ä

    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–π –∫–∞–¥—Ä "snapshot.png" –≤ —Ç–µ–∫—É—â–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏

    cv2.imwrite("snapshot.png", frame)

    print("üì∑ –°–Ω–∏–º–æ–∫ —Å–æ—Ö—Ä–∞–Ω—ë–Ω –∫–∞–∫ snapshot.png")

# –û—Å–≤–æ–±–æ–∂–¥–∞–µ–º —Ä–µ—Å—É—Ä—Å—ã, —Å–≤—è–∑–∞–Ω–Ω—ã–µ —Å –æ–±—ä–µ–∫—Ç–æ–º VideoCapture (–∑–∞–∫—Ä—ã–≤–∞–µ–º –∫–∞–º–µ—Ä—É)

